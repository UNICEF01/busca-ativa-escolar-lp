                                Aprofundam as informações obtidas por meio do alerta e fazem uma análise técnica de cada
                                caso visando a garantia da (re)matrícula das crianças e dos adolescentes.
                            </p>

                            <p>
                                Agentes Comunitários: Fazem a busca ativa de crianças e adolescentes que estão fora da
                                escola e enviam os alertas.
                            </p>
