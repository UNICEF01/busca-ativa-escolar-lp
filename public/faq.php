                    <label class="faq-btn" for="faq-1">
                        O que é a Busca Ativa Escolar?
                    </label>
                    <div class="faq-painel">
                        <div class="faq-inner">
                            <p>
                                A Busca Ativa Escolar é uma plataforma baseada em uma metodologia social e uma
                                ferramenta tecnológica, cedida gratuitamente aos municípios e estados, para colaborar
                                com o enfrentamento da exclusão escolar. A iniciativa foi desenvolvida pelo Fundo das
                                Nações Unidas para a Infância (UNICEF) em parceria com a União Nacional dos Dirigentes
                                Municipais de Educação (Undime), o Colegiado Nacional de Gestores Municipais de
                                Assistência Social (Congemas). A intenção da Busca Ativa Escolar é apoiar os governos na
                                identificação, registro, controle e acompanhamento de crianças e adolescentes que estão
                                fora da escola ou em risco de evasão. Por meio da plataforma, municípios e estados podem
                                registrar e acessar dados concretos que possibilitarão planejar, desenvolver e
                                implementar políticas públicas que contribuam para a inclusão escolar.
                            </p>

                            <p>
                                A iniciativa tem como princípio a “intersetorialidade”, envolvendo diferentes áreas da
                                gestão pública – Educação, Saúde, Assistência Social, Planejamento, entre outras –
                                dentro de uma mesma plataforma. Cada pessoa ou grupo tem um papel específico, que vai
                                desde a identificação de uma criança ou adolescente fora da escola até a tomada das
                                providências necessárias para a matrícula e a permanência do aluno ou aluna na escola.
                            </p>

                            <p>
                                A estratégia da Busca Ativa Escolar parte do princípio que, para assegurar o direito à
                                educação, é preciso considerar a indivisibilidade dos direitos da criança e do
                                adolescente. E, para isso, a intersetorialidade das políticas públicas tem importância
                                estratégica. Somente políticas implementadas de maneira realmente intersetorial poderão
                                garantir, por exemplo, a inclusão, a permanência e a aprendizagem de crianças e
                                adolescentes com deficiência, em abrigos, em cumprimento de medida socioeducativa,
                                egressos ou em risco de trabalho infantil, ou em diversas outras situações de
                                vulnerabilidade social e econômica.
                            </p>

                            <p>
                                Todo o processo é feito pela internet e a ferramenta pode ser acessada em qualquer
                                dispositivo, como computadores de mesa, computadores portáteis, tablets, celulares (SMS)
                                e smartphones.
                            </p>
                        </div>
                    </div>
                </li>
            </ul>

