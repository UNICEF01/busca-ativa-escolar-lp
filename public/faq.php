                                garantir ações intersetoriais.
                            </p>

                            <p>
                                Coordenador Operacional: Planeja, gerencia e acompanha o andamento das ações da Busca
                                Ativa Escolar. Também controla o cadastramento dos usuários que atuarão no âmbito do
                                município/ estado.
                            </p>

                            <p>
                                Supervisores Institucionais: Recebem os alertas sobre crianças e adolescentes que estão
                                fora da escola, fazem os encaminhamentos necessários e providenciam o que for necessário
                                para garantir a (re)matrícula e a permanência na escola.
                            </p>

